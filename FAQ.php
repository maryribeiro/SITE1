    <h2>Porque meu Pen Drive / HD não tem a capacidade demonstrada na Embalagem?</h2>
    <br>
</div>
<hr>
<br>
<p>Muita gente se pergunta isso: “Ué, eu comprei um Pen Drive de 2GB e por que só dá como 1,8GB?”, ou então: “Porque meu HD de 320GB só tem 300GB?”, sem saber que essa é uma coisa muito simples:
    Os discos removíveis, como Pen Drives, e os discos rígidos (HD’s) vêm com o espaço definido pelo fabricante, por exemplo, se um HD é de 160GB, tem 160GB. Só que esses dispositivos são “Plug and Play”, ou seja, basta conectá-los que o sistema irá reconhecê-los. Então, onde ficam os drivers? A resposta é simples: Os drivers desses dispositivos ficam dentro deles mesmos e não podem ser excluídos. Por isso, o tamanho da unidade aparece menor do que o tamanho real.</p>
